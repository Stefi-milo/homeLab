# Conversion to boolean:
#  bool(value) - returns value converted to True of False


# for numbers: 0 False
#              everything else is True

print(f'{bool(0.0)=}')
print(f'{bool(2-2)=}')


print(f'{bool(10)=}')
print(f'{bool(0.1)=}')
print(f'{bool(-100)=}')

# for containers including strings
# (string is a container of letters)
#    for empty (does not contain anything) False
#    for everything else True
print()


print(f'{bool("hello")=}')
print(f'{bool("")=}')           # empty string
print(f'{bool("0")=}')

